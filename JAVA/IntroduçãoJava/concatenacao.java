package IntroduçãoJava;



public class concatenacao {
	public static void main(String [] args) {
		int var1 = 10;
		int var2 = 20;

		//o sinal "+" serve de concatenação, junta 2 negóço.
		System.out.println("A variável a " + var2 + " é o dobro da variável " + var1);
		
		System.out.println(+ var1 +   "+"   + var2  +  "="  + (var1 + var2));
	}
}
